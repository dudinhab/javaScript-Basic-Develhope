/*
Stampa in console i numeri compresi da 0 a -10
*/

//Costruttore per ripetere il conteggio con un conteggio progressivo, con la variabile i che riceve un decremento
for (let i = 0; i >= -10; i--) {
    //Visualizzazione della variabile di conteggio
    console.log(`The number is:${i}`);
    //console.log("The number is:" +i);
}